/**
 * Parses website from description if it follows the format: 
 * "**Website**<br/>[[https://www.example.com/|example.com]]"
 */
const parseWebsite = (desc: string): string => {
  const match = desc.match(/\[\[https?:\/\/([^\|]+)\|/);
  return match ? `https://${match[1]}` : '';
};

